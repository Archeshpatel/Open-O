package org.caisi.PMmodule.web;

public class RatingView {
	private String avrgScore;
	private String vstNumber;
	private String pageName;
	public String getAvrgScore() {
		return avrgScore;
	}
	public void setAvrgScore(String avrgScore) {
		this.avrgScore = avrgScore;
	}
	public String getPageName() {
		return pageName;
	}
	public void setPageName(String pageName) {
		this.pageName = pageName;
	}
	public String getVstNumber() {
		return vstNumber;
	}
	public void setVstNumber(String vstNumber) {
		this.vstNumber = vstNumber;
	}
	
	
}
